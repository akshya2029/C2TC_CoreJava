package com.tns.day2;

public class thirdprogram {
	    public static void main(String[] args) {
	        
	        for (int i = 1; i <= 5; i++) {
	            System.out.println("Number: " + i);
	        }
	  // while loop
        int i = 1;
    
        while (i <= 5) {
            System.out.println("Number: " + i);
            i++;
        }
   // do while 
        int j= 1;
                do {
            System.out.println("Number: " + i);
            j++;
        } while (j <= 5);
    }
}
